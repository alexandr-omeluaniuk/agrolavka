package ss.agrolavka.wrapper;

/**
 * Order wrapper.
 * @author alex
 */
public class OrderDetailsWrapper {
    
    private String city;
    private String comment;
    private String delivery;
    private String district;
    private Long europostLocationId;
    private String europostLocationName;
    private String firstname;
    private String flat;
    private String house;
    private String lastname;
    private String middlename;
    private String phone;
    private String postcode;
    private String region;
    private String street;

    /**
     * @return the city
     */
    public String getCity() {
        return city;
    }
    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }
    /**
     * @return the comment
     */
    public String getComment() {
        return comment;
    }
    /**
     * @param comment the comment to set
     */
    public void setComment(String comment) {
        this.comment = comment;
    }
    /**
     * @return the delivery
     */
    public String getDelivery() {
        return delivery;
    }
    /**
     * @param delivery the delivery to set
     */
    public void setDelivery(String delivery) {
        this.delivery = delivery;
    }
    /**
     * @return the district
     */
    public String getDistrict() {
        return district;
    }
    /**
     * @param district the district to set
     */
    public void setDistrict(String district) {
        this.district = district;
    }
    /**
     * @return the europostLocationId
     */
    public Long getEuropostLocationId() {
        return europostLocationId;
    }
    /**
     * @param europostLocationId the europostLocationId to set
     */
    public void setEuropostLocationId(Long europostLocationId) {
        this.europostLocationId = europostLocationId;
    }
    /**
     * @return the europostLocationName
     */
    public String getEuropostLocationName() {
        return europostLocationName;
    }
    /**
     * @param europostLocationName the europostLocationName to set
     */
    public void setEuropostLocationName(String europostLocationName) {
        this.europostLocationName = europostLocationName;
    }
    /**
     * @return the firstname
     */
    public String getFirstname() {
        return firstname;
    }
    /**
     * @param firstname the firstname to set
     */
    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }
    /**
     * @return the flat
     */
    public String getFlat() {
        return flat;
    }
    /**
     * @param flat the flat to set
     */
    public void setFlat(String flat) {
        this.flat = flat;
    }
    /**
     * @return the house
     */
    public String getHouse() {
        return house;
    }
    /**
     * @param house the house to set
     */
    public void setHouse(String house) {
        this.house = house;
    }
    /**
     * @return the lastname
     */
    public String getLastname() {
        return lastname;
    }
    /**
     * @param lastname the lastname to set
     */
    public void setLastname(String lastname) {
        this.lastname = lastname;
    }
    /**
     * @return the middlename
     */
    public String getMiddlename() {
        return middlename;
    }
    /**
     * @param middlename the middlename to set
     */
    public void setMiddlename(String middlename) {
        this.middlename = middlename;
    }
    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }
    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }
    /**
     * @return the postcode
     */
    public String getPostcode() {
        return postcode;
    }
    /**
     * @param postcode the postcode to set
     */
    public void setPostcode(String postcode) {
        this.postcode = postcode;
    }
    /**
     * @return the region
     */
    public String getRegion() {
        return region;
    }
    /**
     * @param region the region to set
     */
    public void setRegion(String region) {
        this.region = region;
    }
    /**
     * @return the street
     */
    public String getStreet() {
        return street;
    }
    /**
     * @param street the street to set
     */
    public void setStreet(String street) {
        this.street = street;
    }
}
